<b>regole generali</b>
<br>
|spalanca|dot|com| è una comunità. come tale si basa su un concetto molto semplice: il membro.
che non vuol dire il cazzo, quindi d'ora in poi ci si riferirà al membro con 'personaggio'. per diventare
 un personaggio di |spalanca|dot|com| è necessario procedere all'iscrizione, che è gratuita e anonima.
 chi non è un personaggio può solamente consultare il sito. in questa fase tutte le aree del sito sono aperte
  alla consultazione pubblica tranne l'area di gestione utente, ovviamente. il personaggio inoltre può pubblicare
   news, robba, commenti e può creare e partecipare ai forum seguendo le indicazioni che saranno esposte qui sotto.<br>
|spalanca|dot|com| fa uso di cookies. senza troppe menate tecniche questo significa che se avete problemi a loggarvi i cookies su ie vanno abilitati.<br>
<br>
<b>l'iscrizione</b>
<br>ogni personaggio dovrà scegliere uno username di massimo 16 caratteri, questo username non potrà mai essere cambiato.<br>
un altro dato obbligatorio è la password di massimo 16 caratteri, questa può essere cambiata in qualsiasi momento. nel caso la password venga persa o dimenticata non potrà
 più essere recuperata. per ragioni di sicurezza/privacy nessuno ha visione delle password altrui, nemmeno nizzi insomma.<br>
 ogni personaggio può scegliere un'icona che lo contradistinguerà in tutte le sue pubblicazioni sul sito. deve essere un file gif di dimensioni 50x50 pixel e di massimo 64K (animazioni e trasparenze sono consentite).
 in mancanza sarà utilizzata un'icona di default. in ogni momento è possibile cambiare la propria icona<br>
ogni personaggio può specificare la propria e-mail e può scegliere se renderla visibile o meno ai consultatori di |spalanca|dot|com|<br>
ognuno dei dati, ad eccezione dello username, può essere modificato in qualsiasi momento dal pulsante 'gestisciti'.<br>&nbsp;
