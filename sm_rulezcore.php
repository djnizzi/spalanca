Il Totomondiali di |spalanca|dot|com| &egrave; la versione online di un gioco 
  che ha le sue origini nel lontano 1994 in occasione del mondiale USA. Ai tempi 
  c'erano i foglietti... ora abbiamo la tecnologia... tecno... tecno... tecno!<br>
  <br>
  Ora le cose serie: per partecipare al gioco &egrave; necessario essere iscritti 
  a |spalanca|dot|com|. Non ci stancheremo mai di ripeterlo: l'iscrizione &egrave; 
  gratuita e anonima. Anche la semplice consultazione alla sezione Totomondiali sar&agrave; consentita 
  solo agli iscritti. Per partecipare al gioco &egrave; necessario compilare in 
  ogni sua parte la scheda previsioni. La scheda &egrave; personale e unica per 
  ogni partecipante, va compilata prima delle ore 0.00 del 31/05/2002, ed entro 
  quella data sar&agrave; possibile modificarla. Se un iscritto ha gi&agrave; 
  pubblicato la sua scheda e tenta di pubblicarne una nuova questa andr&agrave; 
  a sovrascrivere la scheda precedente. La scheda non &egrave; segreta, sar&agrave; 
  visibile a tutti gli iscritti. Ogni personaggio di |spalanca|dot|com| che compila 
  la scheda si consider&agrave; partecipante al gioco.<br>
  <br>
  La scheda comprende tre sezioni da compilare sulla stessa maschera: nella prima 
  sezione dovr&agrave; essere indicato un risultato e un segno (1, X o 2) per 
  ogni partita della fase dei gironi. Il risultato e il segno di una stessa partita 
  possono anche essere non coerenti. Il programma &egrave; quello ufficiale pubblicato 
  su <a href="http://www.worldcup.com">www.worldcup.com</a>.<br>
  Ogni segno esatto vale 1 punto, ogni risultato esatto vale 3 punti.<br>
  Nella seconda sezione dovranno essere indicate le 16 squadre che andranno agli 
  ottavi di finali, le 8 che andranno ai quarti, le 4 che andranno in semifinale, 
  le 2 finaliste e la squadra campione mondiale. Queste previsioni possono anche 
  non essere coerenti tra loro e con i risultati indicati nella prima sezione 
  (ad. es. posso indiacare la Francia tra le 4 semifinaliste pur non avendola 
  indicata tra le squadre qualificate dai gironi, oppure non so, mettere 4 squadre dello stesso girone tra quelle che passano). Non si pu&ograve; indicare 
  la stessa squadra 2 volte tra le qualificate dello stesso turno (cio&egrave; 
  lo so che non si &egrave; capita sta cosa: non si pu&ograve; mettere 2 volte 
  l'Italia tra le 16 che vanno agli ottavi).<br>
  Ogni squadra qualificata agli ottavi d&agrave; 10 punti. Ogni squadra qualificata 
  ai quarti d&agrave; 20 punti. Ogni squadra qualificata alle semifinali d&agrave; 
  30 punti. Ogni finalista 40 punti. La vincitrice 60 punti!<br>
  Nella terza sezone dovranno essere indicati 5 giocatori diversi da scegliere 
  nelle rose delle 32 squadre. Ogni gol segnato da ognuno di questi 5 vale 3 punti. Gli autogol ovviamente non portano punti.<br>
  Dovr&agrave; inoltre essere indicato il nome di un giocatore che verr&agrave; 
  espulso. L'eventuale espulsione varr&agrave; ben 20 punti, ovviamente solo una 
  volta.<br>
  <br>
  L'inserimento dei risultati e dei marcatori di ogni partita avverà appena possibile a cura degli administrator di |spalanca|dot|com| con riferimento ai dati pubblicati sul sito della  
  <a href=http://www.gazzetta.it>Gazza</a>. I punteggi dei partecipanti al Totomondiali saranno aggiornati in base a risultati e marcatori in automatico
  <br>
  <br>
  In ogni momento sar&agrave; possibile per i partecipanti visualizzare la classifica 
  aggiornata all'ultima partita degli iscritti al gioco (a punti ovviamente).<br>
  <br>
