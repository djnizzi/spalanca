<b>pubblicazione di robba</b>
<br>
il termine 'robba' sta per tutto quello che è creazione dei personaggi di |spalanca|dot|com|.<br>
in questa prima fase per vari motivi tecnici la robba pubblicabile può essere solo immagini jpeg di dimensioni non superiori a 64K.<br>
la robba pubblicata apparirà immediatamente online, quindi non è prevista la moderazione da parte dello staff di |spalanca|dot|com|.<br>
questo non esclude la possibilità da parte dello staff di eliminare ogni pubblicazione ritenuta in qualche modo contraria alle seguenti regole:<br><br>
- niente robba porno, per nessun motivo. sono consentite però fotografia o arte 'erotica'. per capirci ad esempio tette al vento si possono anche mettere, scianti.<br>
- niente robba non originale. che sia coperta da copyright o meno. questa comunità pretende solo le creazioni dei propri personaggi. si considerano originali anche creazioni 'derivate', meglio specificare però l'autore dell'opera originale nella presentazione della vostra pubblicazione<br>
- niente robba palesemente offensiva, razzista, politica ecc.<br><br>
un personaggio trovato a pubblicare ripetutamente robba contraria a queste regole (in ogni caso subito segata), sarà prima avvertito e se persevera bandito da |spalanca|dot|com|, per sempre.
<br>
la proprietà intellettuale di ogni robba pubblicata è dell'autore dell'opera. si assume che questo sia lo stesso pubblicatore a meno che non sia specificato diversamente nella presentazione dell'opera
